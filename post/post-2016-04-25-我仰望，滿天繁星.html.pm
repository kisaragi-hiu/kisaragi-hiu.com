當我抬起頭，仰望著，美麗星空——
那自然界的奇妙意象啊！如此文靜地照耀著、閃爍著……
如我等生靈的守護者般，無聲地看望著、保護著……
即使城市日漸增長、星空漸被蓋過，
無數星塵，依然永恆地遍布天空。

於此夜光佔滅之時，即使電力將回、燈火將明，
來吧！來愛上這都市從未見過的，美妙星空……
即使它將自我等眼前消失，永不復見，
它也將永不消失，永遠地，守護……
